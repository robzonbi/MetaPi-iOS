import SwiftUI

struct TabBarStyle {
    static func configure() {
        let tabBar = UITabBar.appearance()
        tabBar.backgroundColor = UIColor(named: "backgroundWhite")
        tabBar.barTintColor = UIColor(named: "backgroundWhite")
    }
}
